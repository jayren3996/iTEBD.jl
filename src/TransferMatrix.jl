# Symmetry representation
#---------------------------------------------------------------------------------------------------
export symrep
function symrep(
    T,
    U;
    tr::Bool=false
)
    tT = tr ? conj.(T) : T
    M = otrm(T, U, tT)
    de, dv = dominent_eigen(M)
    χ = round(Int, sqrt(length(dv)))
    reshape(dv, χ, χ)
end

